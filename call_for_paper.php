					</h4>
                            <p>
                                Bangladesh Institute of Bank Management (BIBM) has been organizing Annual Banking Conference since 2012, a two day-long international gathering aiming at bringing together experts, academicians and researchers from all over the world to exchange and share knowledge, experience and research outputs on banking and related issues. The Annual Banking Conference-2016 will be held in the last week of November, 2016 at BIBM, Dhaka, Bangladesh. The Conference sessions will cover contemporary issues on banking, finance and the economy, both in the national and international context.
                            </p>
                            <h4>Manuscript Submission, Review Process and Notification of Acceptance</h4>
                            <p>
                                Please e-mail the abstract of your paper to bankingconference@bibm.org.bd by July 31, 2016 and submit the full paper by September 30, 2016. The paper(s) would be reviewed for suitability and notification of acceptance will be announced on or before October 15, 2016. Conference paper must be in English language. The author(s) are advised to follow the following format to prepare the paper:
                            </p>
                            <ul>
                                <li>Abstract Size: within 200 words.</li>
                                <li>Full paper length: 5000 words to 8000 words.</li>
                                <li>Letter font: Time New Roman.</li>
                                <li>Letter/Font Size: 12</li>
                                <li>Line Spacing: Double</li>
                                <li>Paper size: A4 Size</li>
                                <li>Paper Margin: 1″ (Top, Bottom, Right and Left)</li>
                                <li>Reference: Harvard Style <a href="http://libweb.anglia.ac.uk/referencing/harvard.htm" target="_blank">(Link http://libweb.anglia.ac.uk/referencing/harvard.htm)</a> </li>
                                <li>Paper must be submitted in MS-word version</li>
                                <li>The cover page of the paper should include the institutional affiliation, address, phone number and email address of each author.</li>
                            </ul>
                            <h4>Proceedings and Publication Opportunities</h4>
                            <p>The finally selected and presented papers will be published in the conference proceedings and will be hosted on the BIBM website which can be accessed and viewed by the researchers and academicians around the world. There is no publication fee for the finally selected papers. A printed copy of the conference proceedings will be provided to all authors free of cost. In case of co-author, only one author will present the paper in the conference. Again, an author can present only one paper. </p>

                            <h4>Award for Selected Papers</h4>
                            <p>“Reviewers Choice” award will be given to selected papers based on the comments of the review committee. Also each paper presenter will be given a certificate.</p>



