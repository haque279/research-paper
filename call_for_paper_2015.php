                            <p>
                                Bangladesh Institute of Bank Management (BIBM) has been organizing Annual Banking Conference since 2012, a two day-long international gathering aiming at bringing together experts, academicians and researchers from all over the world to exchange and share knowledge, experience and research outputs on banking and related issues. The Annual Banking Conference-2015 will be held in the last week of November, 2015 at BIBM, Dhaka, Bangladesh. The Conference sessions will cover contemporary issues on banking, finance and the economy, both in the national and international context.
